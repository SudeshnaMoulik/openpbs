/* pointer to store handle to loaded external auth library */
static void *libauth_handle = NULL;

/*
 * the function pointer to set logger method for auth lib
 * MUST exist in auth lib
 */
void (*pbs_auth_set_config)(void (*func)(int type, int objclass, int severity, const char *objname, const char *text), char *cred_location) = NULL;

/*
 * the function pointer to create new auth context used by auth lib
 * MUST exist in auth lib
 */
int (*pbs_auth_create_ctx)(void **ctx, int mode, const char *hostname) = NULL;

/*
 * the function pointer to free auth context used by auth lib
 * MUST exist in auth lib
 */
void (*pbs_auth_destroy_ctx)(void *ctx) = NULL;

/*
 * the function pointer to get user, host and realm information from authentication context
 * MUST exist in auth lib
 */
int (*pbs_auth_get_userinfo)(void *ctx, char **user, char **host, char **realm) = NULL;

/*
 * the function pointer to which will process auth handshake data and authenticate user/connection
 * MUST exist in auth lib
 */
int (*pbs_auth_process_handshake_data)(void *ctx, void *data_in, size_t len_in, void **data_out, size_t *len_out, int *is_handshake_done) = NULL;

/*
 * the function pointer to encrypt data
 * Should exist in auth lib if auth lib supports encrypt/decrypt
 */
int (*pbs_auth_encrypt_data)(void *ctx, void *data_in, size_t len_in, void **data_out, size_t *len_out) = NULL;

/*
 * the function pointer to decrypt data
 * Should exist in auth lib if auth lib supports encrypt/decrypt
 */
int (*pbs_auth_decrypt_data)(void *ctx, void *data_in, size_t len_in, void **data_out, size_t *len_out) = NULL;


static int tcp_send_auth_req(int);

/**
 * @brief
 *	tcp_send_auth_req - encodes and sends PBS_BATCH_Authenticate request for external auth
 *
 * @param[in] sock - socket descriptor
 *
 * @return	int
 * @retval	0 on success
 * @retval	-1 on error
 */
static int
tcp_send_auth_req(int sock)
{
	struct batch_reply *reply;
	int rc;
	int am_len = strlen(pbs_conf.auth_method);

	DIS_tcp_funcs();

	if (encode_DIS_ReqHdr(sock, PBS_BATCH_Authenticate, pbs_current_user) ||
		diswui(sock, 0) || /* is_auth_resvport */
		diswui(sock, am_len) || /* auth type length */
		diswcs(sock, pbs_conf.auth_method, am_len) ||
		encode_DIS_ReqExtend(sock, NULL)) { /* auth type */
		pbs_errno = PBSE_SYSTEM;
		return -1;
	}

	if (dis_flush(sock)) {
		pbs_errno = PBSE_SYSTEM;
		return -1;
	}

	reply = PBSD_rdrpy_sock(sock, &rc);

	if (reply == NULL) {
		pbs_errno = PBSE_BADCRED;
		return -1;
	}

	if ((reply->brp_code != 0)) {
		pbs_errno = PBSE_BADCRED;
		PBSD_FreeReply(reply);
		return -1;
	}

	PBSD_FreeReply(reply);

	return 0;
}

/**
 * @brief
 *	recv_auth_token - receive auth token
 *
 * @param[in] fd - file descriptor
 * @param[out] type - token type - context or wrapped msg
 * @param[out] data_out - token string data
 * @param[out] len_out - length of data
 *
 * @return	int
 * @retval	>0	number of characters read
 * @retval	0	if EOD (no data currently avalable)
 * @retval	-1	if error
 * @retval	-2	if EOF (stream closed)
 *
 */
int
recv_auth_token(int fd, int *type, void **data_out, size_t *len_out)
{
	int i = 0;
	void *pkt = NULL;

	*type = 0;
	*data_out = NULL;
	*len_out = 0;

	i = transport_recv(fd, (char *)type, 1);
	if (i != 1)
		return i;

	pkt = malloc(sizeof(int));
	if (pkt == NULL)
		return -1;
	i = transport_recv(fd, pkt, sizeof(int));
	if (i != sizeof(int))
		return -1;
	*len_out = (size_t)ntohl(*((int *) pkt));
	free(pkt);
	if (*len_out <= 0) {
		*len_out = 0;
		return -1;
	}

	*data_out = malloc(*len_out);
	if (*data_out == NULL) {
		*len_out = 0;
		return -1;
	}
	i = transport_recv(fd, *data_out, *len_out);
	if (i != *len_out) {
		free(*data_out);
		*data_out = NULL;
		*len_out = 0;
		return i;
	}

	return i;
}

/**
 * @brief
 *	send_auth_token - assemble and send auth token
 *
 * @param[in] fd - file descriptor
 * @param[in] type - token type - context or wrapped msg
 * @param[in] data - token string data
 * @param[in] len - length of data
 *
 * @return	int
 * @retval	>0	number of characters read
 * @retval	0	if EOD (no data currently avalable)
 * @retval	-1	if error
 * @retval	-2	if EOF (stream closed)
 *
 */
int
send_auth_token(int fd, int type, void *data_in, size_t len_in)
{
	int ndlen = 0;
	int i = 0;
	void *pkt = NULL;
	char *pos = NULL;
	int pktlen = 1 + sizeof(int) + len_in;

	pkt = malloc(pktlen);
	if (pkt == NULL)
		return -1;
	pos = (char *)pkt;

	*pos++ = (char)type;

	ndlen = htonl(len_in);
	memcpy(pos, &ndlen, sizeof(int));
	pos += sizeof(int);

	memcpy(pos, data_in, len_in);

	i = transport_send(fd, pkt, pktlen);
	free(pkt);
	if (i > 0 && i != pktlen)
		return -1;
	return i;
}

/**
 * @brief
 * 	this function handles client side authentication
 *
 * @param[in] fd - socket descriptor
 * @param[in] hostname - server hostname
 * @param[out] ebuf - error buffer
 * @param[in] ebufsz - size of error buffer
 *
 * @return	int
 * @retval	0	success
 * @retval	-1	failure
 *
 */
int
engage_client_auth(int fd, char *hostname, char *ebuf, size_t ebufsz)
{
	void *authctx = NULL;
	void *data_in = NULL;
	size_t len_in = 0;
	void *data_out = NULL;
	size_t len_out = 0;
	int type;
	int is_handshake_done;

	pbs_auth_set_config(NULL, pbs_conf.pbs_home_path);

	if ((authctx = transport_chan_get_extra(fd)) == NULL) {
		if (pbs_auth_create_ctx(&authctx, AUTH_CLIENT, (const char *)hostname)) {
			snprintf(ebuf, ebufsz, "Failed to create auth context");
			pbs_errno = PBSE_SYSTEM;
			return -1;
		}
		transport_chan_set_extra(fd, authctx);
	}

	if (tcp_send_auth_req(fd) != 0) {
		snprintf(ebuf, ebufsz, "Failed to send auth request");
		return -1;
	}

	do {
		if (pbs_auth_process_handshake_data(authctx, data_in, len_in, &data_out, &len_out, &is_handshake_done) != 0) {
			snprintf(ebuf, ebufsz, "Auth handshake failed");
			pbs_errno = PBSE_SYSTEM;
			return -1;
		}

		if (len_in) {
			free(data_in);
			data_in = NULL;
			len_in = 0;
		}

		if (len_out > 0) {
			if (send_auth_token(fd, AUTH_CTX_DATA, data_out, len_out) <= 0) {
				snprintf(ebuf, ebufsz, "Failed to send auth context token");
				pbs_errno = PBSE_SYSTEM;
				free(data_out);
				return -1;
			}
			free(data_out);
			data_out = NULL;
			len_out = 0;
		}

		if (is_handshake_done == 0) {
			/* recieve ctx token */
			if (recv_auth_token(fd, &type, &data_in, &len_in) <= 0) {
				return -1;
			}
			if (type != AUTH_CTX_DATA) {
				free(data_in);
				snprintf(ebuf, ebufsz, "incorrect auth token type");
				pbs_errno = PBSE_SYSTEM;
				return -1;
			}
		} else {
			transport_chan_set_authctx_status(fd, AUTH_STATUS_CTX_READY);
			if (pbs_auth_encrypt_data != NULL && pbs_auth_decrypt_data != NULL)
				transport_chan_set_encrypted(fd);
		}

	} while (is_handshake_done == 0);

	transport_chan_set_extra(fd, authctx);
	return 0;
}

/**
 * @brief
 * 	this function handles authentication related data before process_request()
 *
 * @param[in] fd - socket descriptor
 * @param[in] hostname - server hostname
 * @param[in] clienthost - hostname associate with fd
 * @param[out] ebuf - error buffer
 * @param[in] ebufsz - size of error buffer
 *
 * @return	int
 * @retval	0	success
 * @retval	-1	failure
 *
 */
int
engage_server_auth(int fd, char *hostname, char *clienthost, char *ebuf, size_t ebufsz)
{
	void *authctx = NULL;
	void *data_in = NULL;
	size_t len_in = 0;
	void *data_out = NULL;
	size_t len_out = 0;
	int type;
	int is_handshake_done = 0;

	DIS_tcp_funcs();

	if (transport_chan_get_authctx_status(fd) != (int)AUTH_STATUS_CTX_ESTABLISHING) {
		/*
		 * auth ctx not establishing
		 * consider data as clear text or encrypted data
		 * BUT not auth ctx data
		 */
		return 1;
	}

	if ((authctx = transport_chan_get_extra(fd)) == NULL) {
		if (pbs_auth_create_ctx(&authctx, AUTH_SERVER, clienthost)) {
			snprintf(ebuf, ebufsz, "Failed to create auth context");
			pbs_errno = PBSE_SYSTEM;
			return -1;
		}
		transport_chan_set_extra(fd, authctx);
	}

	if (recv_auth_token(fd, &type, &data_in, &len_in) <= 0)
		return -1;

	if (type != AUTH_CTX_DATA) {
		snprintf(ebuf, ebufsz, "received incorrect auth token");
		pbs_errno = PBSE_SYSTEM;
		free(data_in);
		return -1;
	}

	if (pbs_auth_process_handshake_data(authctx, data_in, len_in, &data_out, &len_out, &is_handshake_done) != 0) {
		snprintf(ebuf, ebufsz, "pbs_auth_process_handshake_data failure");
		pbs_errno = PBSE_SYSTEM;
		free(data_in);
		return -1;
	}

	free(data_in);

	if (len_out > 0) {
		if (send_auth_token(fd, AUTH_CTX_DATA, data_out, len_out) <= 0) {
			snprintf(ebuf, ebufsz, "Failed to send auth context token");
			free(data_out);
			return -1;
		}
	}

	free(data_out);

	if (is_handshake_done) {
		transport_chan_set_authctx_status(fd, AUTH_STATUS_CTX_READY);
		if (pbs_auth_encrypt_data != NULL && pbs_auth_decrypt_data != NULL)
			transport_chan_set_encrypted(fd);
		transport_chan_set_extra(fd, authctx);
	}
	return 0;
}

static char *
_get_load_lib_error(int reset)
{
	if (reset) {
#ifndef WIN32
		(void)dlerror();
#else
		(void)SetLastError(0);
#endif
		return NULL;
	}
#ifndef WIN32
	return dlerror();
#else
	static char buf[LOG_BUF_SIZE + 1] = {'\0'};
	LPVOID lpMsgBuf;
	int err = GetLastError();
	int len = 0;

	FormatMessage(
		FORMAT_MESSAGE_ALLOCATE_BUFFER |
		FORMAT_MESSAGE_FROM_SYSTEM |
		FORMAT_MESSAGE_IGNORE_INSERTS,
		NULL, err,
		MAKELANGID(LANG_NEUTRAL, SUBLANG_DEFAULT),
		(LPTSTR)&lpMsgBuf, 0, NULL);
	strncpy(buf, lpMsgBuf, sizeof(buf));
	LocalFree(lpMsgBuf);
	buf[sizeof(buf) - 1] = '\0';
	len = strlen(buf);
	if (buf[len - 1] == '\n')
		len--;
	if (buf[len - 1] == '.')
		len--;
	buf[len - 1] = '\0';
	return buf;
#endif
}

static void *
_load_lib(char *loc)
{
	(void)_get_load_lib_error(1);
#ifndef WIN32
	return dlopen(loc, RTLD_LAZY);
#else
	return LoadLibrary(loc);
#endif
}

static void *
_load_symbol(void *handle, char *name)
{
	(void)_get_load_lib_error(1);
#ifndef WIN32
	return dlsym(handle, name);
#else
	return GetProcAddress(handle, name);
#endif
}

int
load_auth_lib(void)
{
	static char libloc[MAXPATHLEN + 1] = {'\0'};
	int i = 0;
	char *errmsg = NULL;
	static struct {
		char *name;
		void **var;
		unsigned required;
	} funcs_to_load[] = {
		{ "pbs_auth_set_config", (void **)&pbs_auth_set_config, 1 },
		{ "pbs_auth_create_ctx", (void **)&pbs_auth_create_ctx, 1 },
		{ "pbs_auth_destroy_ctx", (void **)&pbs_auth_destroy_ctx, 1 },
		{ "pbs_auth_get_userinfo", (void **)&pbs_auth_get_userinfo, 1 },
		{ "pbs_auth_process_handshake_data", (void **)&pbs_auth_process_handshake_data, 1 },
		/*
		 * There are possiblity that auth lib only support authentication
		 * but not encrypt/decrypt of data (for example munge auth lib)
		 * so below 2 methods are marked as NOT required
		 */
		{ "pbs_auth_encrypt_data", (void **)&pbs_auth_encrypt_data, 0 },
		{ "pbs_auth_decrypt_data", (void **)&pbs_auth_decrypt_data, 0 },
		{ NULL, NULL, 0 }
	};

	if (libauth_handle || pbs_conf.is_auth_resvport)
		return 0;

	if (libloc[0] == '\0') {
#ifndef WIN32
		snprintf(libloc, MAXPATHLEN, "%s/lib/libauth_%s.so", pbs_conf.pbs_exec_path, pbs_conf.auth_method);
#else
		snprintf(libloc, MAXPATHLEN, "%slib/libauth_%s.dll", pbs_conf.pbs_exec_path, pbs_conf.auth_method);
#endif
		libloc[MAXPATHLEN] = '\0';
	}

	libauth_handle = _load_lib(libloc);
	if (libauth_handle == NULL) {
		errmsg = _get_load_lib_error(0);
		if (errmsg) {
			fprintf(stderr, "%s\n", errmsg);
		} else {
			fprintf(stderr, "Failed to load %s\n", libloc);
		}
		goto err;
	}
	for (i = 0; funcs_to_load[i].name != NULL; i++) {
		*(funcs_to_load[i].var) = _load_symbol(libauth_handle, funcs_to_load[i].name);
		if (funcs_to_load[i].required && *(funcs_to_load[i].var) == NULL) {
			errmsg = _get_load_lib_error(0);
			if (errmsg) {
				fprintf(stderr, "%s\n", errmsg);
			} else {
				fprintf(stderr, "symbol %s not found in %s", funcs_to_load[i].name, libloc);
			}
			goto err;
		}
	}
	return 0;
err:
	unload_auth_lib();
	return 1;
}

void
unload_auth_lib(void)
{
	if (libauth_handle != NULL) {
#ifndef WIN32
		(void)dlclose(libauth_handle);
#else
		(void)FreeLibrary(libauth_handle);
#endif
	}
	libauth_handle = NULL;
	pbs_auth_set_config = NULL;
	pbs_auth_create_ctx = NULL;
	pbs_auth_destroy_ctx = NULL;
	pbs_auth_get_userinfo = NULL;
	pbs_auth_process_handshake_data = NULL;
	pbs_auth_encrypt_data = NULL;
	pbs_auth_decrypt_data = NULL;
}
